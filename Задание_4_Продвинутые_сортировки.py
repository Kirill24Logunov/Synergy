# Функция для перевода десятичного числа в двоичное
def decimal_to_binary(n):
    if n == 0:
        return ""  # Базовый случай: число равно 0, возвращаем пустую строку
    else:
        return decimal_to_binary(n // 2) + str(n % 2)  # Рекурсивный вызов для остатка от деления и целочисленного деления на 2

# Функция для умножения двух чисел
def multiply(x, y):
    if y == 0:
        return 0  # Базовый случай: один из множителей равен 0, возвращаем 0
    else:
        return x + multiply(x, y - 1)  # Рекурсивный вызов с уменьшением второго множителя на 1

# Функция для возведения числа в степень
def power(x, y):
    if y == 0:
        return 1  # Базовый случай: степень равна 0, возвращаем 1
    else:
        return x * power(x, y - 1)  # Рекурсивный вызов с уменьшением степени на 1

# Пример использования функций
num = 13
print("Двоичное представление числа", num, ":", decimal_to_binary(num))

x = 5
y = 3
print("Результат умножения", x, "*", y, ":", multiply(x, y))

a = 2
b = 4
print("Результат возведения", a, "в степень", b, ":", power(a, b))
